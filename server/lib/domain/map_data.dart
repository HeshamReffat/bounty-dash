const int kMapCols = 40;
const int kMapRows = 26;

// ─── Special positions (col, row) ─────────────────────────────────────────────
const kExitPosition = (col: 2, row: 24);

// Spawn points: index 0 = Runner, 1-3 = Guards
const kSpawnPositions = [
  (col: 1,  row: 1),
  (col: 38, row: 1),
  (col: 38, row: 24),
  (col: 20, row: 13),
];

/// Collect all floor tiles that can serve as random artifact spawn points.
/// Excludes tiles adjacent to spawn points and the exit to avoid unfair placement.
List<({int col, int row})> getFloorTiles() {
  final excluded = <String>{};
  for (final s in kSpawnPositions) {
    for (int dr = -2; dr <= 2; dr++) {
      for (int dc = -2; dc <= 2; dc++) {
        excluded.add('${s.col + dc},${s.row + dr}');
      }
    }
  }
  excluded.add('${kExitPosition.col},${kExitPosition.row}');

  final floors = <({int col, int row})>[];
  for (int r = 0; r < kMapRows; r++) {
    for (int c = 0; c < kMapCols; c++) {
      if (kMapData[r][c] == tFloor && !excluded.contains('$c,$r')) {
        floors.add((col: c, row: r));
      }
    }
  }
  return floors;
}

/// Pick [count] random floor tiles for artifacts, spaced at least 4 tiles apart.
List<({int col, int row})> randomArtifactPositions(int count, {int? seed}) {
  final rng = seed != null ? Random(seed) : Random();
  final candidates = getFloorTiles()..shuffle(rng);
  final picked = <({int col, int row})>[];

  for (final tile in candidates) {
    if (picked.length >= count) break;
    // Enforce minimum distance between artifacts
    bool tooClose = false;
    for (final p in picked) {
      final dx = (tile.col - p.col).abs();
      final dy = (tile.row - p.row).abs();
      if (dx + dy < 4) { tooClose = true; break; }
    }
    if (!tooClose) picked.add(tile);
  }
  return picked;
}
